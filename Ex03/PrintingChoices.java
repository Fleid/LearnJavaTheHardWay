public class PrintingChoices
{
	public static void main( String[] args )
	{
		System.out.println( "A" );
		System.out.println( "B" );
		
		System.out.println( "C" );
		System.out.println();
		
		System.out.print( "E" );
		System.out.print( "F" );
		
		System.out.println();
		System.out.println( "G"+"H" );
	}
}